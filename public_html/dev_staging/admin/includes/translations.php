/**
 * Get translation function
 */
function __($key, $lang = null) {
    global $translations;
    
    // Get language from session or default to German
    if ($lang === null) {
        $lang = $_SESSION['admin_lang'] ?? 'de';
    }
    
    // Return translation or key if not found
    return $translations[$lang][$key] ?? $key;
}

/**
 * Set admin language
 */
function setAdminLanguage($lang) {
    if (in_array($lang, ['en', 'de'])) {
        $_SESSION['admin_lang'] = $lang;
        return true;
    }
    return false;
}

/**
 * Get current admin language
 */
function getAdminLanguage() {
    return $_SESSION['admin_lang'] ?? 'de';
}
